    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        String nama = textNama.getText();
	String jenis = jenisBox.getSelectedItem().toString();
	String rating = ratingBox.getSelectedItem().toString();
	String jumlahKalori = kaloriSpn.getValue().toString();

	Object[] data = {nama, jenis, rating, jumlahKalori};
	tabelModel.addRow(data);
        reset();
    }//GEN-LAST:event_submitBtnActionPerformed

    private void reset(){
		textNama.setText("");
		jenisBox.setSelectedIndex(0);
		ratingBox.setSelectedIndex(0);
		kaloriSpn.setValue(0);
	}
    

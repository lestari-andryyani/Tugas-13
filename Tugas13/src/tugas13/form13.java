    private void newFilter(String keyword){
            RowFilter rowfilter = null;

		try{
			rowfilter = RowFilter.regexFilter(keyword, 0);
		} catch (java.util.regex.PatternSyntaxException e){
			System.out.println(e.getMessage());
		}
		tabelSorter.setRowFilter(rowfilter);
	}
    
    private void cariBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBtnActionPerformed
        // TODO add your handling code here:
        String keyword = textCari.getText();
		newFilter(keyword);
    }//GEN-LAST:event_cariBtnActionPerformed

    private void detailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailBtnActionPerformed
        // TODO add your handling code here:
        int selectedBaris = tableDataMenu.getSelectedRow();
	int baris = tableDataMenu.convertRowIndexToModel (selectedBaris);
	String nama = tableDataMenu.getValueAt(baris, 0).toString();
	String jenis = tableDataMenu.getValueAt(baris, 1).toString();
	String rating = tableDataMenu.getValueAt(baris, 2).toString();
	String jumlahKalori = tableDataMenu.getValueAt(baris, 3).toString();

	String hasil = "Nama  : " + nama + "\nJenis : " + jenis 
		+  "\nRating : " + rating + "\nJumlah Kalori : " + jumlahKalori;

	JOptionPane.showMessageDialog(null, hasil);
    }//GEN-LAST:event_detailBtnActionPerformed

    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        // TODO add your handling code here:
        int selectedBaris = tableDataMenu.getSelectedRow();
	int Baris = tableDataMenu.convertRowIndexToModel(selectedBaris);

	String nama = tabelModel.getValueAt(Baris, 0).toString();

	int jawaban = JOptionPane.showConfirmDialog(null, "Hapus Data? " + nama);
	if (jawaban == JOptionPane.YES_OPTION){
		tabelModel.removeRow(Baris);
	}
    }//GEN-LAST:event_hapusBtnActionPerformed

